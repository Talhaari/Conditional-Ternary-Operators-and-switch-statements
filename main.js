// switch statements
var dayofweek = "tuesday";
switch (dayofweek) {
    case "wednesday":
        console.log("go to market");
        break;
    case "monday":
        console.log("read a book");
        break;
    case "tuesday":
        console.log("cleaning room");
        break;
    case "wednesday":
        console.log("planting");
    default:
        console.log("make a typescript program");
}
